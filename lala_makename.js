function run() {
    const body = {
        tail: false,
        head: false,
        wings: false,
        hands: false,
        legs: false,
        fingers: false,
        toes: false,
        horns: false,
        animal_ears: false
    };
    Object.keys(body).forEach((x)=>{
        Object.defineProperty(body, x, {
            value: getRandom(Creatures).body_parts[x]
        });
    });
    const similarities = {
        tail: false,
        head: false,
        wings: false,
        hands: false,
        legs: false,
        fingers: false,
        toes: false,
        horns: false,
        animal_ears: false
    };
    Object.keys(similarities).forEach((x)=>{
        similarities[x] = getRandom(Creatures.filter((y)=>y.body_parts[x])).name;
    });
    return {
        body,
        similarities
    };
}
function generateRace(name) {
    const race = new Race();
    if (name) race.name = name;
    return race;
}
