function generateName(lenn) {
    if (!lenn) lenn = 4 + Math.floor(Math.random() * 5);
    let name = getRandom(alphabet);
    for(let pointer = 0; pointer < lenn - 1; ++pointer){
        if (pointer === lenn - 2) {
            name += getRandom(__default4.find((x)=>x.letter === name.charAt(name.length - 1).toUpperCase())?.possible ?? vowels);
        } else {
            if (consonants.includes(name.charAt(name.length - 1)) && consonants.includes(name.charAt(name.length - 2))) {
                name += getRandom(vowels);
            } else if (vowels.includes(name.charAt(name.length - 1)) && vowels.includes(name.charAt(name.length - 2)) && vowels.includes(name.charAt(name.length - 3))) {
                name += getRandom(consonants);
            } else {
                name += getRandom(__default4.find((x)=>x.letter === name.charAt(name.length - 1).toUpperCase())?.possible ?? vowels);
            }
        }
    }
    return name;
}
